
//use this example to imagine the indexes of a 2D Array
public class NestedForExample {
    public static void main(String[] args) {
        //loop of i
        for (int i = 0; i < 3; i++) {
            //loop of j
            for (int j = 0; j < 3; j++) {
                System.out.print(i + "" + j +" ");
            }//end of i
            System.out.println();
        }//end of j
    }
}  